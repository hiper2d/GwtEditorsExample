package com.hiper2d.client.domain;

public class PhoneNumber {
    private String type;
    private String number;

    public PhoneNumber() {
        this.type = type;
        this.number = number;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }
}
